public class Book {
	
	//List of private attributes.
	private String title;
	private int year;
	private String publish;
	private int pages;

	//Default constructor
	Book(){
		this.title = "Foundations of Computer Science";
		this.year = 2019;
		this.publish = "The University of Adelaide";
		this.pages = 1320; 
	}

	//Parametric Constructor
	Book(String title, int year, String publish, int pages) {
		this.title = title;
		this.year = year;
		this.publish = publish;
		this.pages = pages;
	}

	//Creating accessors for Title, Year, Publish, and Page. 
	public String getTitle() {
		return this.title;
	}

	public int getYear() {
		return this.year;
	}

	public String getPublish() {
		return this.publish;
	}
 	 
	public int getPages() {
		return this.pages;
	}

	//Creating mutators for Title, Year, Publish, and Page. 
	public void setTitle(String title) {
		this.title = title;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public void setPublish(String publish) {
		this.publish = publish;
	}

	public void setPages(int pages) {
		this.pages = pages;
	}
}
