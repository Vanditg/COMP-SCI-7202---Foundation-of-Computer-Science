abstract class Animal {

	//List of private attributes.
	private String name;
	private double weight;
	private String favouriteFood;
	private int age;

	//Default constructor
	Animal(){
		this.name = "Cat";
		this.weight = 5.0d;
		this.favouriteFood = "Milk";
		this.age = 2; 
	}

	//Parametric Constructor
	Animal(String name, double weight, String favouriteFood, int age) {
		this.name = name;
		this.weight = weight;
		this.favouriteFood = favouriteFood;
		this.age = age; 
	}

	//Creating accessors for Name, Weight, Food, and Age. 
	public String getName() {
		return this.name;
	}

	public double getWeight() {
		return this.weight;
	}

	public String getFavouriteFood() {
		return this.favouriteFood;
	}
 	 
	public int getAge() {
		return this.age;
	}

	//Creating mutators for Name, Weight, Food, and Age. 
	public void setName(String name) {
		this.name = name;
	}

	public void setWeight(double weight) {
		this.weight = weight;
	}

	public void setFavouriteFood(String favouriteFood) {
		this.favouriteFood = favouriteFood;
	}

	public void setAge(int age) {
		this.age = age;
	}
	abstract public void makeSound();
}
