public class Patient {

    //Instatiating private variables. 
    private static int totalPatient;
    // The unique id for the patient
    private int id;
    // can be 1-5, from not urgent to resuscitation
    private int triageLevel;
    private String phoneNumber;
    private String name;
    private String location;

    //Parametric constructor. 
    public Patient(String name, String phoneNumber, int triageLevel, String location) {
        this.name = name;
        this.phoneNumber = phoneNumber;
        this.triageLevel = triageLevel;
        this.location = location;
        this.totalPatient ++;
        this.id = this.totalPatient;
    }

    //Defining accessor - getName
    public String getName() {
        return name;
    }

    //Defining mutator - setName
    public void setName(String name) {
        this.name = name;
    }

    //Defining accessor - getPhoneNumber
    public String getPhoneNumber() {
        return phoneNumber;
    }

    //Defining mutator - setPhoneNumber
    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    //Defining accessor - getTriageLevel
    public int getTriageLevel() {
        return triageLevel;
    }

    //Defining mutator - setTriageLevel
    public void setTriageLevel(int triageLevel) {
        this.triageLevel = triageLevel;
    }

    //Defining accessor - getLocation
    public String getLocation() {
        return location;
    }

    //Defining mutator - setLocation
    public void setLocation(String location) {
        this.location = location;
    }

    //Defining accessor - getID
    public int getId() {
        return this.id;
    }
}

