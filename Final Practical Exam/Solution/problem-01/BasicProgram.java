import java.lang.*; 
import java.util.*;

public class BasicProgram {

    //Defineing method sum that returns sum of two integers. 
    public int sum(int a, int b) {
        return a + b;
    }

    //Defining method div which returns division of two double numbers and throws exception for the given case. 
    public double div(double a, double b) throws UnsupportedOperationException {
        if (b == 0) {
            throw new UnsupportedOperationException("Not possible division by zero.");
        } else {
            return a / b;
        }

    }

    //Defining method to displayArray which print outs the array and throws exception for the given case.
    public void displayArray(int[] array) throws UnsupportedOperationException {

        if (array.length == 0) {
            throw new UnsupportedOperationException("No elements.");
        } else {

            System.out.print("[ ");
            for (int i = 0; i < array.length; i++) {
                if (array.length != 0) {
                	System.out.print(array[i] + ", ");
                }
                else {
                	System.out.print("]; ");
                }
                
            }
            
        }

    }

}
