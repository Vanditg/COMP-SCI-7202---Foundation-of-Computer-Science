public class Node {
		
	//Defining private variables.
	private Node next;
	private Student info;

	//Default constructor - setting null to everthing.
	Node() {
		this.next = null;
		this.info = null;
	}

	//Parametric constructor - tmpStudent
	Node(Student tmpStudent) {
		this.next = null;
		this.info = tmpStudent;
	}
	
	//Defining accessors - getNext, getInfo.
	public Node getNext() {
		return this.next;
	}

	public Student getInfo() {
		return this.info;
	}

	//Defining mutators - setInfo, setNext.
	public void setInfo(Student info) {
		this.info = info;
	}

	public void setNext(Node next) {
		this.next = next;
	}
}